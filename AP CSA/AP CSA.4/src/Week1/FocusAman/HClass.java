package FocusAman;

public class HClass {
    private String name;
    private String teacher;
    private int per;

    public HClass(String name, String teacher, int per){
        this.name = name;
        this.teacher = teacher;
        this.per = per;
    }

    public String getName() {
        return name;
    }
    public String getTeacher() {
        return teacher;
    }
    public int getPer() {
        return per;
    }

}
